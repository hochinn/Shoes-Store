    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int price = 0;
        if (!OD.isEmpty()) {
            for (int i = 0; i < OD.size(); i++) {
                price = price + OD.get(i).getOrdersDatailPriceAll();
            }
            Orders add = new Orders();
            add.setEmpID(empID);
            DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd");
            LocalDate localDate = LocalDate.now();
            //2016/11/16
            add.setOrderDate(dtf.format(localDate));
            add.setPayProductTotal(price);
            int idO = OrdersDao.insert(add);
            for (int i = 0; i < OD.size(); i++) {
                Orders_Detail oo = new Orders_Detail();
                oo = OD.get(i);
                oo.setOrdersID(idO);
                
                Orders_DetailDao.insert(oo);
            }
            OD = new ArrayList<Orders_Detail>();
            tableOrder_Detail.setData(OD);
            JOptionPane.showMessageDialog(this, "สั่งซื้อสำเร็จ!!!");
        } else {
            JOptionPane.showMessageDialog(this, "ไม่มีสินค้า!!!");
        }
    }//GEN-LAST:event_jButton3ActionPerformed


