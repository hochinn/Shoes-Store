    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        if (Dseart != 0) {
            int item = Integer.valueOf(jComboBox2.getSelectedItem().toString());
            int a = 0;
            Orders_Detail od = new Orders_Detail();
            for (int i = 0; i < OD.size(); i++) {
                if (OD.get(i).getProductID() == Dseart) {

                    OD.get(i).setOrdersDetailSum(item + OD.get(i).getOrdersDetailSum());
                    OD.get(i).setOrdersDatailPriceAll(productDao.getProduct(Dseart).getProPrice()*OD.get(i).getOrdersDetailSum());
                    tableOrder_Detail.setData(OD);

                    a++;
                    break;
                }
            }
            if (a == 0) {
                od.setOrders_detailID(OD.size());
                od.setOrdersDetailSum(item);
                od.setProductID(Dseart);
                int price = 0;
                for (int i = 0; i < item; i++) {
                    price = price + (productDao.getProduct(Dseart).getProPrice()*od.getOrdersDetailSum());
                }
                od.setOrdersDatailPriceAll(price);
                od.setOrdersID(0);
                OD.add(od);
                tableOrder_Detail.setData(OD);
            }
        }


    }//GEN-LAST:event_jButton1ActionPerformed

